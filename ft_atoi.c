#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int num;

	i = 0;
	num = 0;
	sign = 1;
	while ((str[i] == '\t') || (str[i] == '\n') || (str[i] == ' ') ||
			(str[i] == '\r') || (str[i] == '\v') ||
			(str[i] == '\f'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num * sign);
}
